package com.generic;

public class Pojo {
	
	private static String token;
	private static int idDataSchema;
	private static String templateId;
	public static String templateName;
	public static int idApp;

	
	
	
	
	
	public static int getIdApp() {
		return idApp;
	}

	public static void setIdApp(int idApp) {
		Pojo.idApp = idApp;
	}

	public static String getTemplateName() {
		return templateName;
	}

	public static void setTemplateName(String templateName) {
		Pojo.templateName = templateName;
	}

	
	

	public static String getToken() {
		return token;
	}

	public static void setToken(String token) {
		Pojo.token = token;
	}
	

	public static String getTemplateId() {
		return templateId;
	}

	public static void setTemplateId(String templateId) {
		Pojo.templateId = templateId;
	}
	public static int getIdDataSchema() {
		return idDataSchema;
	}

	public static void setIdDataSchema(int idDataSchema) {
		Pojo.idDataSchema = idDataSchema;
	}
	

}
